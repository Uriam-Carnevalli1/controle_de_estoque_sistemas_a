private void carregarTabela(List<Produto> produtos) {
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setRowCount(0); // limpa tabela

    for (Produto p : produtos) {
        modelo.addRow(new Object[]{
            p.getIdProduto(),
            p.getNome(),
            p.getQuantidade(),
            p.getEstoqueMinimo()
        });
    }
}

